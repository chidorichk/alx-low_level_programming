#include "main.h"
#include <stdlib.h>
#include <stdio.h>

/**
 *print_sign - check whether a letter is lower or not
 *@n: parameter c allow to handle the character
 *Return: 1 or 0 or -1
 */

int print_sign(int n)
{
if (n > 0)
{
_putchar('+');
return (1);
}
else if (n == 0)
{
_putchar('0');
return (0);
}
else
{
_putchar('-');
return (-1);
}
}
